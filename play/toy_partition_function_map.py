# Compute τ(n) from the product formula
def ramanujan_tau(N):
    """Compute Ramanujan tau function τ(n) for n ≤ N."""
    # Δ(q) = q ∏_{n≥1} (1-q^n)^24
    # We compute the q-expansion of ∏(1-q^n)^24 up to q^N
    # then shift by q^1

    coeffs = [0] * (N + 2)
    coeffs[0] = 1  # start with 1

    # Multiply by (1-q^k)^24 for k=1..N
    for k in range(1, N + 1):
        # (1-q^k)^24: use the expansion
        # We need to multiply the current series by (1-q^k)^24
        # For efficiency, compute (1-x)^24 = Σ C(24,j) (-1)^j x^j
        from math import comb
        # Apply the multiplication in-place
        # We need to go from high to low to avoid overwriting
        binom_coeffs = [((-1)**j) * comb(24, j) for j in range(25)]
        new_coeffs = [0] * (N + 2)
        for i in range(N + 1):
            if coeffs[i] == 0:
                continue
            for j in range(25):
                idx = i + j * k
                if idx > N:
                    break
                new_coeffs[idx] += coeffs[i] * binom_coeffs[j]
        coeffs = new_coeffs

    # Now Δ(q) = q × Σ coeffs[n] q^n, so τ(n) = coeffs[n-1]
    tau = {}
    for n in range(1, N + 1):
        tau[n] = coeffs[n - 1]
    return tau

