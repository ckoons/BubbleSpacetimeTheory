# Compute partition function
@lru_cache(maxsize=500)
def partition(n):
    if n < 0:
        return 0
    if n == 0:
        return 1
    result = 0
    k = 1
    while True:
        # Euler's pentagonal theorem
        g1 = k * (3*k - 1) // 2
        g2 = k * (3*k + 1) // 2
        if g1 > n:
            break
        sign = (-1)**(k+1)
        result += sign * partition(n - g1)
        if g2 <= n:
            result += sign * partition(n - g2)
        k += 1
    return result

