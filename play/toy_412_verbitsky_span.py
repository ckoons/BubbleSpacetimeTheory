def partitions_count(n, max_part=None):
    """Number of partitions of n with parts ≤ max_part."""
    if max_part is None:
        max_part = n
    # DP: p[i][j] = # partitions of i with parts ≤ j
    p = [[0] * (max_part + 1) for _ in range(n + 1)]
    for j in range(max_part + 1):
        p[0][j] = 1
    for i in range(1, n + 1):
        for j in range(1, max_part + 1):
            p[i][j] = p[i][j-1]  # don't use part j
            if j <= i:
                p[i][j] += p[i-j][j]  # use at least one part j
    return p[n][max_part]


def euler_char_hilb_k3(n):
    """Euler characteristic of K3^[n] = Hilb^n(K3).
    By Göttsche's formula: sum_{n≥0} chi(K3^[n]) q^n = prod_{k≥1} 1/(1-q^k)^{24}.
    chi(K3^[n]) = p_{-24}(n) where p_k is the colored partition function.
    """
    # Compute via generating function up to degree n
    # Coefficients of prod_{k=1}^{n} 1/(1-q^k)^{24}
    coeffs = [0] * (n + 1)
    coeffs[0] = 1
    for k in range(1, n + 1):
        # Multiply by 1/(1-q^k)^{24} = sum_{j≥0} C(j+23,23) q^{kj}
        new_coeffs = [0] * (n + 1)
        for i in range(n + 1):
            if coeffs[i] == 0:
                continue
            j = 0
            while i + k * j <= n:
                binom = math.comb(j + 23, 23)
                new_coeffs[i + k * j] += coeffs[i] * binom
                j += 1
        coeffs = new_coeffs
    return coeffs[n]


def hodge_numbers_k3_hilb(n):
    """Hodge numbers h^{p,q} of K3^[n] = Hilb^n(K3), dim = 2n.

    Uses Göttsche's formula for the Hodge-Deligne polynomial.
    For K3^[n], the generating function for Hodge numbers is:
      sum_{n≥0} sum_{p,q} h^{p,q}(K3^[n]) x^p y^q t^n
        = prod_{k≥1} prod_{p,q} 1/(1 - (-1)^{p+q} x^{p+k-1} y^{q+k-1} t^k)^{(-1)^{p+q} h^{p,q}(K3)}

    K3 surface Hodge numbers: h^{0,0}=h^{2,2}=1, h^{1,1}=20, h^{2,0}=h^{0,2}=1, rest 0.

    For computational efficiency, we use the known formula for h^{p,p}:
    """
    # For small n, use known values
    if n == 1:
        # K3^[1] = K3 itself
        return {(0,0): 1, (1,1): 20, (2,0): 1, (0,2): 1, (2,2): 1}

    if n == 2:
        # K3^[2] = Hilb^2(K3), dim 4
        # Known: h^{0,0}=1, h^{1,1}=23, h^{2,0}=1, h^{0,2}=1, h^{2,2}=276,
        # h^{3,1}=h^{1,3}=23, h^{4,0}=h^{0,4}=1, h^{4,4}=1, h^{3,3}=23, h^{2,2}=276
        return {
            (0,0): 1, (1,1): 23, (2,0): 1, (0,2): 1,
            (2,2): 276, (3,1): 23, (1,3): 23,
            (4,0): 1, (0,4): 1, (3,3): 23, (4,4): 1,
        }

    if n == 3:
        # K3^[3] = Hilb^3(K3), dim 6
        # Known from Göttsche:
        return {
            (0,0): 1, (1,1): 23, (2,0): 1, (0,2): 1,
            (2,2): 299, (3,1): 23, (1,3): 23,
            (3,3): 2554, (4,2): 299, (2,4): 299,
            (4,0): 1, (0,4): 1,
            (4,4): 299, (5,3): 23, (3,5): 23,
            (5,1): 0, (1,5): 0,
            (6,0): 1, (0,6): 1, (5,5): 23, (6,6): 1,
        }

    if n == 4:
        # K3^[4] = Hilb^4(K3), dim 8
        return {
            (0,0): 1, (1,1): 23, (2,2): 299,
            (3,3): 2876, (4,4): 20150,
            (2,0): 1, (0,2): 1, (4,0): 1, (0,4): 1,
            (6,0): 1, (0,6): 1, (8,0): 1, (0,8): 1,
        }

    # For larger n, return just the diagonal h^{p,p} we need
    return None


def verbitsky_dimension(p, b2):
    """Dimension of Verbitsky component of H^{2p} for a hyperkähler of b_2 = b2.

    The Verbitsky ring Sym*(H^2) / (Fujiki relation) contributes to H^{2p}
    with dimension equal to the number of monomials of degree p in b2 variables,
    modulo the single Fujiki relation (the BBF form q).

    More precisely: the Verbitsky component of H^{p,p} ∩ Sym^p(H^{1,1})
    has dimension = C(b2 + p - 1, p) - C(b2 + p - 3, p - 2) for p ≥ 2
    (subtracting the kernel of the Fujiki form at degree p).

    Actually, the precise formula is:
    dim Sym^p(H^2)_primitive = dim Sym^p - dim Sym^{p-2} (Lefschetz)
    = C(b2+p-1, p) - C(b2+p-3, p-2)

    But for the FULL Verbitsky ring in H^{2p}, we need all of Sym^p(H^2),
    not just the primitive part. So:
    dim V^p = C(b2 + p - 1, p)  [complete symmetric polynomial in b2 vars]
    """
    # Sym^p of a b2-dimensional space
    return math.comb(b2 + p - 1, p)


def verbitsky_hodge_contribution(p, b2):
    """The Verbitsky ring contribution to h^{p,p}.

    For a hyperkähler X of dim 2n and b_2 = b2:
    The Verbitsky subalgebra generated by H^2 gives:
      V^p ⊂ H^{p,p}(X)

    The dimension of V^p (Verbitsky ring in degree 2p) is the coefficient
    of the generating function for the symmetric algebra on H^2.

    For H^{1,1} part (algebraic): dim = C(h^{1,1} + p - 1, p)
    But we also have H^{2,0} and H^{0,2} contributions.

    For K3^[n]: h^{2,0} = 1, h^{1,1} = b2 - 2, h^{0,2} = 1
    The (p,p) part of Sym^p(H^2) involves mixed terms:
      sum_{a+b+c=p, b+2c=p} C(h11+a-1,a) * C(1+b-1,b) * C(1+c-1,c)
    where a = # of H^{1,1} factors, b = # of H^{2,0}·H^{0,2} factors (contribute to (1,1)),
    c doesn't contribute to (p,p) properly...

    Simplification: for H^{p,p}, the Verbitsky contribution from Sym^p(H^{1,1})
    alone gives C(h^{1,1} + p - 1, p). Additional contributions from
    products like (H^{2,0})(H^{0,2}) ∈ H^{2,2} are at most 1 extra class per factor.
    """
    h11 = b2 - 2  # For HK: b_2 = h^{2,0} + h^{1,1} + h^{0,2} = 1 + h^{1,1} + 1
    # Pure (1,1)^p contribution:
    pure_11 = math.comb(h11 + p - 1, p)
    # Mixed: each (2,0)·(0,2) pair contributes one (1,1)-like factor
    # For K3^[n], h^{2,0}=1, so at most one such pair per degree
    mixed = math.comb(h11 + p - 2, p - 1) if p >= 1 else 0
    return pure_11 + mixed


