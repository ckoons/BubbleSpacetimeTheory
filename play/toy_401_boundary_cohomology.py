def long_exact_sequence():
    """
    The long exact sequence of the pair (X̄, ∂X̄):

    ... → H^k_c(X) → H^k(X̄) → H^k(∂X̄) → H^{k+1}_c(X) → ...

    where H^k_c = compactly supported cohomology.

    By Poincaré-Lefschetz duality: H^k_c(X) ≅ H^{2d-k}(X)^∨ where d = dim_R X = 10.

    The boundary map δ: H^k(∂X̄) → H^{k+1}_c(X) is the residue/restriction map.

    For Hodge classes:
    - A Hodge class α ∈ H^{p,p}(X̄) restricts to a class α|_∂ ∈ H^{2p}(∂X̄)
    - If α|_∂ = 0, then α comes from H^{2p}_c(X) = interior cohomology
    - If α|_∂ ≠ 0, we need to show α|_∂ is algebraic on ∂X̄

    The key: BOTH cases are handled.
    - Interior: theta lift (Proposition 3.1)
    - Boundary: Hodge known at each stratum
    """
    # Degrees where boundary contributes to H^{p,p}
    # P₁ (dim_C = 3) can contribute boundary classes in degrees 0,2,4,6
    # P₂ (dim_C = 1) can contribute boundary classes in degrees 0,2
    # Cusps (dim 0) contribute only in degree 0

    boundary_hodge = {}
    for p in range(6):  # H^{p,p} for p = 0,...,5
        degree = 2 * p
        contributors = []

        # P₁ boundary: D_IV^3 has H^{q,q} for q = 0,1,2,3
        # contributes to H^{2p}(X̄) if 2q ≤ 2·3 = 6, i.e. q ≤ 3
        # The Gysin map shifts degree by 2·codim(P₁) = 2·2 = 4
        # So P₁ boundary contributes to H^{2q+4}(X̄), i.e. p = q+2
        if 0 <= p - 2 <= 3:
            q = p - 2
            contributors.append(f'P₁: H^{{{q},{q}}}(D_IV^3) [Gysin shift +4]')

        # P₂ boundary: modular curve has H^{q,q} for q = 0,1
        # Gysin shift: 2·codim(P₂) = 2·4 = 8
        # contributes to H^{2q+8}(X̄), i.e. p = q+4
        if 0 <= p - 4 <= 1:
            q = p - 4
            contributors.append(f'P₂: H^{{{q},{q}}}(Γ\\H) [Gysin shift +8]')

        # Cusps: Gysin shift 2·5 = 10, contributes to H^{10}(X̄) = H^{5,5}
        if p == 5:
            contributors.append('Cusps: H^{0,0}(pt) [Gysin shift +10]')

        # Interior (theta lift) always contributes
        contributors.append(f'Interior: theta lift onto H^{{{p},{p}}}')

        boundary_hodge[p] = contributors

    return boundary_hodge


