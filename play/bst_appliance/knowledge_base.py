def get_categories():
    """Return sorted list of unique categories."""
    return sorted(set(p["category"] for p in PREDICTIONS))


def get_by_category(category):
    """Return predictions in a given category."""
    return [p for p in PREDICTIONS if p["category"] == category]


def get_by_id(theorem_id):
    """Return a prediction by theorem ID."""
    for p in PREDICTIONS:
        if p["id"] == theorem_id:
            return p
    return None
