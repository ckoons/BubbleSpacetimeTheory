    # Sample some commutators
    elem_list = list(elements.values())
    n_samples = min(100, len(elem_list))
    is_abelian = True
    for i in range(n_samples):
        for j in range(i+1, min(i+10, n_samples)):
            AB = elem_list[i] @ elem_list[j]
