    lines.append("SECTION 2: COMPUTING G = ⟨S, T⟩")
    lines.append("=" * 72)
    lines.append("")

    n = 7  # matrix size

    # We'll generate the group by computing all products of S and T
    # until no new elements appear.
    #
    # Key: we need to identify matrices up to numerical precision.
    # We'll hash matrices by rounding to a grid.

    def mat_key(M, tol=8):
        """Create a hashable key from a matrix by rounding."""
        rounded = np.round(M, decimals=tol)
        # Normalize phase: multiply by exp(-i·arg(first nonzero entry))
        # to remove overall phase ambiguity
        return tuple(rounded.flatten())

    def matrices_equal(A, B, tol=1e-6):
        return np.max(np.abs(A - B)) < tol

    # Start with identity, S, T, T^{-1}
    I_mat = np.eye(n, dtype=complex)
    T_inv = np.conj(T)  # T is diagonal unitary, so T^{-1} = T†

    # Generate group elements
    elements = {}
    elements[mat_key(I_mat)] = I_mat

    # Queue-based generation
    queue = [I_mat, S, T]
    generators = [S, T, T_inv]

    lines.append("  Generating group by closure under multiplication...")
    lines.append(f"  Generators: S (order 2), T (order 32)")
    lines.append("")

    iteration = 0
    max_iter = 100000  # safety limit

    while queue and iteration < max_iter:
        current = queue.pop(0)
        for gen in generators:
            # Right multiply
            product = current @ gen
            key = mat_key(product)
            if key not in elements:
                elements[key] = product
                queue.append(product)
            # Left multiply
            product = gen @ current
            key = mat_key(product)
            if key not in elements:
                elements[key] = product
                queue.append(product)
        iteration += 1

        # Progress report every 1000 iterations
        if iteration % 5000 == 0:
            lines.append(f"    ... iteration {iteration}, |G| so far: {len(elements)}")

    group_order = len(elements)
    lines.append(f"  Group generation complete after {iteration} iterations")
    lines.append(f"  |G| = {group_order}")
    lines.append("")

    # Prime factorization
    def factorize(n):
        factors = {}
        d = 2
        while d * d <= n:
            while n % d == 0:
                factors[d] = factors.get(d, 0) + 1
                n //= d
            d += 1
        if n > 1:
            factors[n] = factors.get(n, 0) + 1
        return factors

    factors = factorize(group_order)
    factor_str = " × ".join(f"{p}^{e}" if e > 1 else str(p) for p, e in sorted(factors.items()))
    lines.append(f"  |G| = {group_order} = {factor_str}")
    lines.append("")

    primes = sorted(factors.keys())
    lines.append(f"  Prime factors: {primes}")
