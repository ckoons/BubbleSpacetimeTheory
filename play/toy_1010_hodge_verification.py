    # Hodge numbers h^{p,q} for smooth quadric Q^n:
    # Q^5: odd-dimensional quadric in P^6
    # For odd n: h^{p,q} = δ_{p,q} for 0 ≤ p,q ≤ n, p+q ≠ n
    # For p+q = n: h^{p,q} = 0 (no middle Hodge class for odd quadrics)

    # Hodge diamond for Q^5 (dim 5):
    # h^{0,0} = 1
    # h^{1,1} = 1
    # h^{2,2} = 1
    # h^{3,3} = h^{2,3} = 0 (odd: middle cohomology vanishes for Hodge)
    # h^{4,4} = 1
