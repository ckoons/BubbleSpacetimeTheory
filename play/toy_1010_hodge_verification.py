    nc_check = nc_derived == N_c
    print(f"\n  N_c = n_C - rank = {n_C} - {RANK} = {nc_derived}: {nc_check}")

    passed = c1_check and chi_check and nc_check
    print(f"  [{'PASS' if passed else 'FAIL'}] T2: Chern classes encode BST integers")
    return passed


