BST_ATOMS = {2, 3, 5, 6, 7, 11, 13, 17, 24, 137}
SHIFTS = {0, 1, -1, 2, -2, 3, -3}   # 0 (no shift), ±1, ±rank, ±N_c


def smooth(n, B):
    """Check if n is B-smooth (all prime factors ≤ B)."""
    if n <= 1: return True
    d = 2
    while d * d <= n:
        while n % d == 0:
            if d > B: return False
            n //= d
        d += 1
    if n > 1 and n > B: return False
    return True


def bst_decomposable(target, max_factors=4, max_iterations=2000):
    """Search for BST decomposition of target up to depth max_factors.
    Returns (success, expression) or (False, '').

    Allowed operations:
    - Product of up to max_factors BST atoms
    - Plus or minus a shift from SHIFTS
    """
    if target in BST_ATOMS:
        return True, str(target)
    if target == 0:
        return True, "0"
    if target == 1:
        return True, "1"
    if target < 0:
        ok, expr = bst_decomposable(-target, max_factors)
        return ok, ("-" + expr) if ok else expr

    # Generate products of atoms up to max_factors
    iterations = [0]

    def try_product(remaining, current_factors, base):
        if iterations[0] > max_iterations:
            return None
        iterations[0] += 1
        if remaining == 1 or len(current_factors) == max_factors:
            # Final value is base; check if base ± shift = target
            for s in SHIFTS:
                if base + s == target:
                    if s == 0:
                        return " * ".join(map(str, current_factors)) or "1"
                    sign = "+" if s > 0 else "-"
                    return f"({'·'.join(map(str, current_factors)) or '1'}) {sign} {abs(s)}"
            return None
        for a in BST_ATOMS:
            if a > remaining:
                continue
            if a > target * 2:
                continue
            new_base = base * a
            if new_base > target * 5:
                continue
            result = try_product(remaining // a if remaining % a == 0 else 1,
                                  current_factors + [a], new_base)
            if result is not None:
                return result
        # Also try standalone: factors form product, check if equals target±shift
        for s in SHIFTS:
            if base + s == target:
                if s == 0:
                    return "·".join(map(str, current_factors)) or "1"
                sign = "+" if s > 0 else "-"
                return f"({'·'.join(map(str, current_factors)) or '1'}) {sign} {abs(s)}"
        return None

    # Try all starting atoms
    for a in BST_ATOMS:
        result = try_product(target // a if target % a == 0 else 1, [a], a)
        if result is not None:
            return True, result
    # Also try pure shifts of zero
    for s in SHIFTS:
        if target == s:
            return True, f"{s}"
    return False, ""


