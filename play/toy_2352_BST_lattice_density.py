def null_decomposable(target, max_factors=4):
    """Same as bst_decomposable but with null atoms."""
    if target in null_atoms:
        return True
    if target <= 1:
        return True
    # Quick check: simple product or product+shift
    for a in null_atoms:
        if target % a == 0:
            other = target // a
            if other in null_atoms or other == 1:
                return True
            for b in null_atoms:
                if other % b == 0 and (other // b) in null_atoms:
                    return True
    for a in null_atoms:
        for b in null_atoms:
            for s in SHIFTS:
                if a * b + s == target:
                    return True
                if a + b + s == target:
                    return True
    return False

