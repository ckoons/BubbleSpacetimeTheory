# WEYL GROUP of BC_2: 8 elements
# ============================================================
# Generated by sign changes and permutation of (nu1, nu2)

def weyl_elements():
    """Return all 8 Weyl group elements as matrices acting on (nu1, nu2)."""
    # W(BC_2) = (Z/2)^2 x S_2 = sign changes x permutations
    elements = []
    labels = []
    for s1 in [1, -1]:
        for s2 in [1, -1]:
            for perm in [False, True]:
                def action(nu, s1=s1, s2=s2, perm=perm):
                    a, b = nu
                    if perm:
                        a, b = b, a
                    return (s1 * a, s2 * b)
                sign_str = ("+" if s1 > 0 else "-") + ("+" if s2 > 0 else "-")
                perm_str = "P" if perm else "I"
                label = f"({sign_str},{perm_str})"
                elements.append(action)
                labels.append(label)
    return elements, labels

