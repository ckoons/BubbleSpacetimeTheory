def check_regularity(nu):
    """
    Check that <w^{-1}*nu, alpha^v> is NOT a non-positive integer
    for any (w, alpha) combination.

    Returns (all_regular, details) where details is a list of
    (w_label, alpha_label, inner_product, distance_to_nearest_pole).
    """
