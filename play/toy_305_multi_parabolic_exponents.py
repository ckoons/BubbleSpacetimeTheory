# ═══════════════════════════════════════════════════════════════════════
# §1 — ROOT SYSTEM B₂ FOR SO₀(5,2)
# ═══════════════════════════════════════════════════════════════════════

# Convention (following BST_CFunction_RatioTheorem.md):
#   Positive roots: e₁, e₂ (short), e₁+e₂, e₁-e₂ (long)
#   Simple roots: β₁ = e₁-e₂ (long), β₂ = e₂ (short)
#   Multiplicities: m_short = n-2 = 3, m_long = 1

# Root as (coefficient of e₁, coefficient of e₂)
E1 = (1, 0)       # short root e₁
E2 = (0, 1)       # short root e₂
E1_PLUS_E2 = (1, 1)   # long root e₁+e₂
E1_MINUS_E2 = (1, -1)  # long root e₁-e₂

POSITIVE_ROOTS = [E1, E2, E1_PLUS_E2, E1_MINUS_E2]
ROOT_NAMES = {E1: 'e₁', E2: 'e₂', E1_PLUS_E2: 'e₁+e₂', E1_MINUS_E2: 'e₁-e₂'}

def root_type(r):
    """Short (e₁, e₂) or long (e₁±e₂)"""
    return 'short' if r in [E1, E2] else 'long'

def multiplicity(r):
    """Root multiplicity for SO₀(5,2): short=3, long=1"""
    return 3 if root_type(r) == 'short' else 1

# Simple roots
BETA1 = E1_MINUS_E2  # long
BETA2 = E2           # short

N = 5  # complex dimension
M_SHORT = N - 2  # = 3
M_LONG = 1

# Half-sum of positive roots (weighted by multiplicities)
# ρ = (1/2) Σ m_α · α
def compute_rho():
    r1 = Fraction(0)
    r2 = Fraction(0)
    for root in POSITIVE_ROOTS:
        m = multiplicity(root)
        r1 += Fraction(m, 2) * root[0]
        r2 += Fraction(m, 2) * root[1]
    return (r1, r2)

RHO = compute_rho()

# ═══════════════════════════════════════════════════════════════════════
# §2 — PARABOLIC SUBGROUPS
# ═══════════════════════════════════════════════════════════════════════

# Standard parabolics correspond to subsets of simple roots kept in the Levi.
# P₀ (minimal/Borel): Levi = torus, no simple roots
# P₁ (omit β₂): Levi contains β₁ = e₁-e₂ → Levi ≅ GL(1) × SO₀(3,2)
# P₂ (omit β₁): Levi contains β₂ = e₂ → Levi ≅ GL(2) × SO₀(1,2)

def levi_roots(simple_roots_in_levi):
    """Positive roots generated by the given simple roots."""
    levi = set()
    for s in simple_roots_in_levi:
        levi.add(s)
    # For B₂, the only non-simple positive roots are e₁ = β₁+β₂ and e₁+e₂ = β₁+2β₂
    # Check if combinations of Levi simple roots give other positive roots
    if BETA1 in simple_roots_in_levi and BETA2 in simple_roots_in_levi:
        levi.add(E1)           # β₁ + β₂ = (e₁-e₂) + e₂ = e₁
        levi.add(E1_PLUS_E2)   # β₁ + 2β₂ = (e₁-e₂) + 2e₂ = e₁+e₂
    return levi

PARABOLICS = {
    'P₀': {
        'name': 'Minimal (Borel)',
        'levi_simples': [],
        'levi_desc': 'GL(1)²',
        'L_functions': 'ξ(s) ratios',
    },
    'P₁': {
        'name': 'Maximal (omit β₂)',
        'levi_simples': [BETA1],
        'levi_desc': 'GL(1) × SO₀(3,2) ≅ GL(1) × Sp(4)',
        'L_functions': 'L(s,π) for Siegel cusp forms on Sp(4)',
    },
    'P₂': {
        'name': 'Maximal (omit β₁)',
        'levi_simples': [BETA2],
        'levi_desc': 'GL(2) × SO₀(1,2)',
        'L_functions': 'L(s,f×g) for GL(2) cusp forms',
    },
}

def roots_outside_levi(parabolic_key):
    """Positive roots NOT in the Levi factor."""
    levi = levi_roots(PARABOLICS[parabolic_key]['levi_simples'])
    return [r for r in POSITIVE_ROOTS if r not in levi]

def rho_P(parabolic_key):
    """Half-sum of positive roots outside the Levi (weighted by multiplicities)."""
    outside = roots_outside_levi(parabolic_key)
    r1 = Fraction(0)
    r2 = Fraction(0)
    for root in outside:
        m = multiplicity(root)
        r1 += Fraction(m, 2) * root[0]
        r2 += Fraction(m, 2) * root[1]
    return (r1, r2)

