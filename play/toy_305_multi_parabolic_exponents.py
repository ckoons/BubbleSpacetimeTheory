    # ── §3: ρ_P vectors ─────────────────────────────────────────────
    print(f"\n§3. ρ_P VECTORS")
    rho_vectors = {}
    for pk in PARABOLICS:
        rp = rho_P(pk)
        rho_vectors[pk] = rp
        print(f"  {pk}: ρ_P = ({float(rp[0]):.1f}, {float(rp[1]):.1f}),  |ρ_P|² = {float(rp[0]**2 + rp[1]**2):.4f}")

