# §3 — EXPONENT FORMULAS
# ═══════════════════════════════════════════════════════════════════════

# The heat kernel test function is h(λ) = exp(-t(|λ|² + |ρ|²)).
# A pole of the scattering matrix at spectral parameter λ₀ contributes
# a residue proportional to exp(-t·E) where:
#   E = |ρ|² - |λ₀|²  (note: off-unitary-axis, so this is complex in general)
#
# For each root α outside the Levi, the c-function factor creates poles
# at ⟨λ, α^∨⟩ = zero of ξ or L-function, shifted by 0, 1, ..., m_α-1.
#
# The coroot α^∨ = 2α/⟨α,α⟩:
#   short roots: |α|² = 1, so α^∨ = 2α
#   long roots: |α|² = 2, so α^∨ = α
#
# The spectral parameter at a pole from root α, shift j, zero at s = σ+iγ:
#   ⟨λ, α^∨⟩ = σ + iγ + j
#
# This constrains ONE linear combination of (λ₁, λ₂). The other component
# is integrated over (Gaussian integral for the heat kernel — contributes
# a smooth prefactor, not an exponential).
#
# The EXPONENT (real part) from root α at shift j and zero at Re(s) = σ:
#
#   Re(E) = |ρ|² - (σ+j)² · ⟨α^∨, α^∨⟩⁻¹ · |α|² - (remaining)
#
# More precisely, the pole condition fixes ⟨λ, α^∨⟩ = σ + j + iγ.
# Write λ = c·α^∨ + λ_⊥ where λ_⊥ ⊥ α^∨.
# Then c = (σ+j+iγ)/|α^∨|².
# |λ|² = |c|²·|α^∨|² + |λ_⊥|² = (σ+j)²+γ²)/|α^∨|² + |λ_⊥|²
#
# The λ_⊥ integration gives a smooth factor. The EXPONENTIAL content
# (what Mandelbrojt sees) is:
#   Re(E_α,j) = |ρ|² - ((σ+j)² + γ²)/|α^∨|²
#
# For the Mandelbrojt argument, we need the IMAGINARY part of the
# exponent (the oscillatory part) to be unique. The imaginary part is:
#   Im(E_α,j) = -2(σ+j)γ/|α^∨|²   (this gives the "frequency")
#
# Two exponents collide iff both Re and Im parts match.

def coroot_norm_sq(root):
    """|α^∨|² = 4/|α|² for the coroot."""
    alpha_sq = root[0]**2 + root[1]**2
    return Fraction(4, alpha_sq)

def exponent_real_part(root, j, sigma):
    """
    Real part of the heat kernel exponent for a zero at Re(s) = sigma,
    root α, shift j.

    Re(E) = |ρ|² - (σ+j)² / |α^∨|²

    (The γ² term and λ_⊥ terms are the same for all exponents with the
    same γ, so they cancel in the distinctness comparison.)
    """
    rho_sq = RHO[0]**2 + RHO[1]**2
    av_sq = coroot_norm_sq(root)
    return rho_sq - Fraction((sigma + j)**2, 1) / av_sq

def exponent_imag_coefficient(root, j, sigma):
    """
    Imaginary part coefficient: Im(E) = -2(σ+j)γ / |α^∨|²
    The "frequency" seen by Mandelbrojt is the coefficient of γ.
    """
    av_sq = coroot_norm_sq(root)
    return Fraction(-2 * (sigma + j), 1) / av_sq


