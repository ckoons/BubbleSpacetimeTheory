def frobenius_symmetrize(op, T, n_pow):
    """Average op over cyclic group generated by T."""
    avg = np.zeros_like(op)
    T_pow = np.eye(op.shape[0], dtype=complex)
    T_inv = T.conj().T
    T_inv_pow = np.eye(op.shape[0], dtype=complex)
    for k in range(n_pow):
        avg += T_pow @ op @ T_inv_pow
        T_pow = T_pow @ T
        T_inv_pow = T_inv_pow @ T_inv
    return avg / n_pow

