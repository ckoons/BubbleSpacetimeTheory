    print(f"  φ = (1 + √n_C)/rank = (1 + √5)/2 = {phi_BST:.6f}")
    print(f"  φ actual          = {phi_obs:.6f}")
    print(f"  Match exact: {phi_BST == phi_obs}")
    check("φ = (1+√n_C)/rank", phi_BST, phi_obs, tol=1e-15)

    print(f"\n  Note: continued fraction of φ = [1; 1, 1, 1, ...]")
    print(f"  Generated by the BST integer 1 (trivial), but ITS value")
    print(f"  involves n_C and rank.")

