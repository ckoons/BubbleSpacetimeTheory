# Parabolic (unipotent): some eigenvalues = 1 with nontrivial Jordan blocks
#
# Elliptic: all eigenvalues on the unit circle
#
# For gamma in Gamma(137): gamma = I + 137*A.
# Eigenvalues of gamma = 1 + 137 * eigenvalues of A.
# If A != 0, eigenvalues are displaced from 1 by O(137).
