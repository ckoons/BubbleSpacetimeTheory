# ======================================================================
# gamma in SO(Q, Z) iff:
#   gamma^T Q gamma = Q  (preserves Q)
#   det(gamma) = 1       (special orthogonal)
#   gamma has integer entries
#
# Generators: reflections in integer vectors v with Q(v) != 0:
#   R_v(x) = x - 2 Q(x,v)/Q(v,v) * v
#
# For Q with integer coefficients, R_v is integer-valued when v is integer
# and 2Q(x,v)/Q(v,v) is integer for all integer x.
# This means Q(v,v) | 2Q(x,v) for all x, i.e., Q(v,v) | 2.
# So Q(v,v) in {1, -1, 2, -2}.

def Q_form(v):
    """Q(v) = v^T Q v = sum v_i^2 * Q_diag_i"""
    return sum(v[i]**2 * Q_diag[i] for i in range(g))

def Q_bilinear(u, v):
    """Q(u,v) = u^T Q v"""
    return sum(u[i] * v[i] * Q_diag[i] for i in range(g))

def reflection(v):
    """Reflection matrix R_v: R_v(x) = x - 2Q(x,v)/Q(v,v) * v"""
    qvv = Q_form(v)
    if qvv == 0:
        return None
    n = len(v)
    R = np.eye(n, dtype=float)
    for i in range(n):
        for j in range(n):
            R[i][j] -= 2.0 * v[i] * v[j] * Q_diag[j] / qvv
    return R

