#   - The "compact directions" (5 of them) stay on the circle
#
# For loxodromic gamma: char poly p(x) has degree 7.
# The loxodromic eigenvalues come in reciprocal pairs: lambda, 1/lambda.
# So p(x) has the form: (x-1)^k * q(x) where q is reciprocal.

def classify_element(M):
    """Classify a matrix as elliptic, parabolic, or loxodromic."""
    eigenvalues = np.linalg.eigvals(M)
    # Check if all eigenvalues are on the unit circle
    abs_eigs = np.abs(eigenvalues)
    off_circle = np.sum(np.abs(abs_eigs - 1.0) > 1e-6)

    if off_circle == 0:
        if np.allclose(M, np.eye(len(M))):
            return "identity"
        # Check for Jordan blocks (parabolic)
        if np.allclose(M @ M, np.eye(len(M))) or np.allclose(np.linalg.matrix_power(M, 137), np.eye(len(M))):
            return "elliptic"
        return "elliptic"  # All eigenvalues on circle
    else:
        return "loxodromic"  # Some eigenvalues off the circle

