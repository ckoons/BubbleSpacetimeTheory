# Quadratic form Q = x1^2+x2^2+x3^2+x4^2+x5^2-x6^2-x7^2
# Metric matrix J = diag(1,1,1,1,1,-1,-1)
J = np.diag([1, 1, 1, 1, 1, -1, -1]).astype(float)

def Q_form(v):
    """Evaluate Q(v) = v^T J v."""
    v = np.array(v, dtype=float)
    return v @ J @ v

def Q_inner(u, v):
    """Q-inner product: u^T J v."""
    return np.array(u, dtype=float) @ J @ np.array(v, dtype=float)

def reflection(v):
    """
    Reflection in the hyperplane orthogonal to v (with respect to Q).
    sigma_v(x) = x - 2*Q(x,v)/Q(v) * v
    Returns 7x7 integer matrix.
    """
    v = np.array(v, dtype=float)
    qv = Q_form(v)
    if abs(qv) < 1e-10:
        raise ValueError(f"Cannot reflect in null vector {v}")
    # sigma = I - 2*v*v^T*J / Q(v)
    # (because Q(x,v) = x^T J v, so the outer product gives v*(v^T J))
    Jv = J @ v
    sigma = np.eye(7) - 2 * np.outer(v, Jv) / qv
    return np.round(sigma).astype(int)

def is_in_SO_Q(A):
    """Check if integer matrix A is in SO(Q,Z): A^T J A = J and det=1."""
    A = np.array(A, dtype=float)
    prod = A.T @ J @ A
    if not np.allclose(prod, J, atol=1e-6):
        return False
    if abs(det(A) - 1) > 0.1:
        return False
    return True

def classify_element(A):
    """
    Classify an SO(5,2) element by its eigenvalues.
    Returns: ('identity', 'elliptic', 'parabolic', 'hyperbolic-1', 'hyperbolic-2')
    and the displacement vector (ell_1, ell_2).
    """
    evals = eigvals(np.array(A, dtype=float))

    # Sort by |eigenvalue|
    real_evals = []
    unit_evals = []
    for e in evals:
        if abs(abs(e) - 1) < 1e-6:
            unit_evals.append(e)
        else:
            real_evals.append(e)

    # Hyperbolic: eigenvalues with |e| != 1
    pos_real = sorted([e.real for e in real_evals if e.real > 1.01], reverse=True)

    if len(pos_real) == 0:
        if all(abs(e - 1) < 1e-6 for e in evals):
            return 'identity', 0.0, 0.0
        elif any(abs(e - 1) < 1e-6 and abs(e.imag) < 1e-6 for e in evals):
            # Check for parabolic (eigenvalue 1 with Jordan block)
            return 'elliptic', 0.0, 0.0
        else:
            return 'elliptic', 0.0, 0.0
    elif len(pos_real) == 1:
        ell1 = np.log(pos_real[0])
        return 'hyperbolic-1', ell1, 0.0
    elif len(pos_real) >= 2:
        ell1 = np.log(pos_real[0])
        ell2 = np.log(pos_real[1])
        if ell1 < ell2:
            ell1, ell2 = ell2, ell1
        return 'hyperbolic-2', ell1, ell2
    else:
        return 'unknown', 0.0, 0.0

