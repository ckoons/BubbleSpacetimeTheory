def orbital_weight_rank2(ell1, ell2):
    ell1, ell2 = mpf(ell1), mpf(ell2)
    D = abs(2*mpsinh(ell1/2))**m_short * \
        abs(2*mpsinh(ell2/2))**m_short * \
        abs(2*mpsinh((ell1+ell2)/2))**m_long * \
        abs(2*mpsinh((ell1-ell2)/2))**m_long
    if D == 0:
        return mpf('inf')
    return 1 / D

