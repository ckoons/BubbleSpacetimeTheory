    # ====================================================================
    print("\n[Section 6] Is +rank a forced shift in SO(2)-equivariant K-theory?")
    print("-" * 72)

    # The +2 in Pin(2) K-theory is forced by representation theory
    # (KO_Pin(2)(pt) has a specific 2-rank summand from chi_0 + chi_s,
    # not fittable to any other value).
    #
    # The restriction to SO(2) preserves the rank (= 2) of this summand
    # because:
    #   1. chi_0 -> 1 (1-dim)
    #   2. chi_s -> 1 (1-dim, distinct from chi_0 in Pin(2) but
    #                  collapses to same in SO(2))
    #   3. Total dim 1 + 1 = 2 preserved
    #
    # So the +2 lands in SO(2)-summand as a FORCED 2-dim contribution,
    # not as a fittable parameter.

