    check("N_max = N_c^3 * n_C + rank = 137",
          N_max, 137)

    # This toy ESTABLISHES: the +2 = rank shift IS forced in
    # Pin(2)-equivariant K-theory by the 2-rank-of-1-dim-reps structure.
