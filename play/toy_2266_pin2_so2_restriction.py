    check("dim(U_0) = 1", so2_dims['U_0'], 1)
    check("dim(U_n) = 2 for n >= 1", so2_dims['U_1'], 2)

    # Complex characters of SO(2) = t^n, n in Z. Each 1-dim complex.
    # Real form: U_n (n >= 1) is the underlying real rep of (t^n + t^{-n}).

    check("SO(2) is identity component of Pin(2)", True, True)

    # ====================================================================
    # SECTION 3 — Restriction map
    # ====================================================================
    print("\n[Section 3] Restriction Pin(2) -> SO(2) on generators")
    print("-" * 72)

    # chi_0 |_SO(2) = trivial 1-dim = U_0 = 1
    chi_0_restricted = restrict_to_SO2(chi_0)
    check("chi_0 |_SO(2) = 1 (trivial 1-dim)",
          chi_0_restricted, 1)

