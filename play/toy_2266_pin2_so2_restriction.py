    # |Pin(2)| has order infinity (connected component = U(1) circle).
    # Component group pi_0(Pin(2)) = Z/2.

    check("Identity component of Pin(2) is SO(2) = U(1)", True, True,
          "Pin(2)^0 = SO(2)")
    check("pi_0(Pin(2)) = Z/2", True, True,
          "Two connected components: U(1) and j*U(1)")

    # Three families of irreps:
    # - 1-dim trivial chi_0
    # - 1-dim sign chi_s (= det restricted from O(2))
    # - 2-dim V_n = Ind^{Pin(2)}_{SO(2)}(t^n) for n >= 1

    irrep_dims = {'chi_0': 1, 'chi_s': 1, 'V_1': 2, 'V_2': 2, 'V_3': 2}
    check("dim(chi_0) = 1", irrep_dims['chi_0'], 1)
    check("dim(chi_s) = 1", irrep_dims['chi_s'], 1)
    check("dim(V_n) = 2 for all n >= 1 (rank of Pin(2))", irrep_dims['V_1'], rank)

