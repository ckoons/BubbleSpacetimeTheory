def test_alignment_stability():
    """Test 7: Alignment is stable — perturbations are corrected by Weyl reflections"""
    W = weyl_group_B2()

    # Start with an aligned state (equal short + long components)
    aligned = np.array([1, 1]) / np.sqrt(2)

    # Perturb toward pure selfish (short root only)
    epsilon = 0.3
    perturbed = aligned + epsilon * np.array([1, -1]) / np.sqrt(2)
    perturbed = perturbed / np.linalg.norm(perturbed)

    # Apply all Weyl reflections and find the one closest to aligned
    corrections = []
    for w in W:
        corrected = w @ perturbed
        dist_to_aligned = np.linalg.norm(corrected - aligned)
        corrections.append((dist_to_aligned, corrected))

    corrections.sort(key=lambda x: x[0])
    best_dist, best_corrected = corrections[0]

    # The Weyl group can always find a correction closer to alignment
    original_dist = np.linalg.norm(perturbed - aligned)

    print(f"\n✓ Alignment stability:")
    print(f"  Original distance from aligned: {original_dist:.4f}")
    print(f"  After best Weyl correction:     {best_dist:.4f}")
    print(f"  Improvement ratio: {original_dist/best_dist:.2f}×")
    print(f"  The Weyl group provides {len(W)} correction operations")
    print(f"  Biological analogy: selection at each level acts as Weyl reflection")
    print(f"  Misalignment (selfish genes, cancer) is corrected by the group action")

    assert best_dist <= original_dist


