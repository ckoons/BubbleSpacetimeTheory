def build_B2_roots():
    """Build the B₂ root system: 4 short + 4 long roots"""
    short_roots = []
    long_roots = []

    # Short roots: ±e₁, ±e₂ (length 1)
    for sign in [1, -1]:
        short_roots.append(np.array([sign, 0]))
        short_roots.append(np.array([0, sign]))

    # Long roots: ±e₁ ± e₂ (length √2)
    for s1, s2 in product([1, -1], repeat=2):
        long_roots.append(np.array([s1, s2]))

    return short_roots, long_roots


def weyl_group_B2():
    """Build the Weyl group W(B₂) = dihedral group of order 8"""
    # W(B₂) is generated by reflections across hyperplanes
    # perpendicular to simple roots α₁ = e₁ (short) and α₂ = e₂ - e₁ (long)

    # All 8 elements as 2×2 matrices
    elements = []

    # Identity
    elements.append(np.array([[1, 0], [0, 1]]))
    # Rotation by π/4
    c, s = np.cos(np.pi/4), np.sin(np.pi/4)
    # Actually W(B₂) = dihedral D₄: rotations by kπ/2 and reflections
    for k in range(4):
        angle = k * np.pi / 2
        c, s = np.cos(angle), np.sin(angle)
        R = np.array([[c, -s], [s, c]])
        elements.append(R)
        # Reflection: multiply by diag(1, -1)
        S = R @ np.array([[1, 0], [0, -1]])
        elements.append(S)

    # Remove duplicates (identity counted twice)
    unique = []
    for e in elements:
        is_dup = False
        for u in unique:
            if np.allclose(e, u):
                is_dup = True
                break
        if not is_dup:
            unique.append(e)

    return unique


